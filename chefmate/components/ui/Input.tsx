import React from 'react';

interface InputProps extends React.InputHTMLAttributes<HTMLInputElement> {
  label?: string;
  error?: string;
}

export const Input: React.FC<InputProps> = ({ label, error, className = '', ...props }) => {
  return (
    <div className="w-full">
      {label && (
        <label className="block text-sm font-semibold text-chef-dark mb-2">
          {label}
        </label>
      )}
      <input
        className={`w-full px-4 py-3 bg-chef-surface border border-transparent rounded-xl text-chef-black placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-chef-green/20 focus:border-chef-green transition-all ${
          error ? 'border-red-500 bg-red-50' : ''
        } ${className}`}
        {...props}
      />
      {error && <p className="mt-1 text-xs text-red-500">{error}</p>}
    </div>
  );
};